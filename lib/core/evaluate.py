def calc_dists(preds, target, normalize): #preds & target are the same shape -> (25,17, 2)
    preds = preds.astype(np.float32)
    #print('preds in (evaluate.py) is ', preds)
    target = target.astype(np.float32)
    #print('target in (evaluate.py) is ', target)
    dists = np.zeros((preds.shape[1], preds.shape[0]))
    for n in range(preds.shape[0]):
        for c in range(preds.shape[1]):
            if target[n, c, 0] > 1 and target[n, c, 1] > 1:
                normed_preds = preds[n, c, :] / normalize[n] #retrieves the x,y values from the certain image and divides by normalization vector
                normed_targets = target[n, c, :] / normalize[n]
                dists[c, n] = np.linalg.norm(normed_preds - normed_targets)
            else:
                dists[c, n] = -1
    return dists


def dist_acc(dists, thr=0.5):
    ''' Return percentage below threshold while ignoring values with a -1 '''
    dist_cal = np.not_equal(dists, -1)
    print('dist_cal is ', dist_cal)
    num_dist_cal = dist_cal.sum()
    print('num_dist_cal is ', num_dist_cal)
    if num_dist_cal > 0:
        dis = np.less(dists[dist_cal], thr).sum() * 1.0 / num_dist_cal
        print('dis is ', dis)
        return dis
    else:
        return -1


